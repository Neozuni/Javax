package looping.self.test;

public class BreakTotal100Test {
	public static void main(String[] args) {
		int i = 0;
		int sum = 0;
		
		while(true){
			if(i >= 100) break;
			i++;
			sum += i;
			System.out.println("Cuurent i : " + i + "\nCurrent Sum : " + sum);
		}
		
		/*
		 * while문 이용
		 * 무한루핑을 돌면서 1씩 증가된다...
	     * 증가된 값을 계속 합산해 나가는데...합산한 값이100을 넘어설때 반복문을 빠져나오도록 로직을 작성
	     * 최종적으로 그때의 합산된 값을 출력.
	     */
	}
}

















